    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        String idp = txId.getText();
        String nmp = txNama.getText();
        String usr = txUser.getText();
        String pw1 = String.valueOf(txPass.getPassword());
        String pw2 = String.valueOf(txPassUlangi.getPassword());
        String lvl = String.valueOf(txLevel.getSelectedItem());
        
        try{
            st = cn.createStatement();
            if(idp.equals("") || nmp.equals("") || usr.equals("") || pw1.equals("") || pw2.equals("") || lvl.equals("")) {
                JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if(!pw1.equals(pw2)){
                JOptionPane.showMessageDialog(null, "Data password harus sama", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if(btSimpan.getText().equals("Simpan")) {
                String cek = "SELECT * FROM tb_pengguna WHERE id_pengguna = '" + idp + "'";
                rs = st.executeQuery(cek);
                if(rs.next()){
                    JOptionPane.showMessageDialog(null, "Ups.. id pengguna sudah ada!");
                }else{
                    String sql = "INSERT INTO tb_pengguna VALUES ('" 
                            + idp + "', '"
                            + nmp + "', '"
                            + usr + "', '"
                            + pw1 + "', '"
                            + lvl + "')";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
                    bersihkanFrom();
                    tampilData("");
                }
            }else{
                String sqlU = "UPDATE tb_pengguna SET"
                        + " nama_pengguna = '" + nmp + "',"
                        + " username = '" + usr + "',"
                        + " level = '" + lvl + "'"
                        + " WHERE id_pengguna = '" + idp + "'";
                st.executeUpdate(sqlU);
                    JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
                bersihkanFrom();
                tampilData("");
                btSimpan.setText("Simpan");
                txId.setEditable(true);
            }
        }catch (SQLException e){
            System.err.println(e);
        }
    }//GEN-LAST:event_btSimpanActionPerformed

