    private void tampilData(String q) {
        int jd = 0;
        try{
            cn = koneksi_db.KoneksiDB.BuatKoneksi();
            st = cn.createStatement();
            
            String sql;
            if(q.equals("")){
                sql = "SELECT * FROM tb_pengguna";
            }else{
                sql = "SELECT * FROM tb_pengguna WHERE "
                        + "nama_pengguna LIKE '%" + q + "%'"; 
            }
            rs = st.executeQuery(sql);
            
            DefaultTableModel model = new DefaultTableModel();
            
            model.addColumn("ID");
            model.addColumn("Nama Pengguna");
            model.addColumn("Username");
            model.addColumn("Level");
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()){
                Object[] data = {
                    rs.getString("id_pengguna"),
                    rs.getString("nama_pengguna"),
                    rs.getString("username"),
                    rs.getString("level")
                };
                
                model.addRow(data);
                tbUser.setModel(model);
                jd++;
            }
            jmUs.setText(jd + "");
        } catch (SQLException e){
            System.err.println(e);
        }
    }
    
