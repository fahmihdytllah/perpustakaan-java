    private void btHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btHapusActionPerformed
        // TODO add your handling code here:
        String ida = txId.getText();
        if(ida.equals("")){
            JOptionPane.showMessageDialog(null, "Data belum dipilih!");
        }else{
            try{
                int knf = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION);
                if(knf == 0){
                    st = cn.createStatement();
                    String sql = "DELETE FROM tb_anggota WHERE id_anggota = '" + ida + "'";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
                    tampilData("");
                    bersihkanFrom();
                    btSimpan.setText("Simpan");
                    txId.setEditable(true);
                }
            }catch(SQLException e){
                System.err.println(e);
            }
        }
    }//GEN-LAST:event_btHapusActionPerformed

